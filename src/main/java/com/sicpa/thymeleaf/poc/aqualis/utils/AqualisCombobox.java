package com.sicpa.thymeleaf.poc.aqualis.utils;

public class AqualisCombobox {
	
	private String label;
	private String value;
	
	public AqualisCombobox(String label, String value) {
		this.label = label;
		this.value = value;
	}
	
	public String getLabel() {
		return label;
	}
	public String getValue() {
		return value;
	}
	

}
